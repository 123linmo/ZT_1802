package com.api;

public class Math_test {

	public static void main(String[] args) {
		System.out.println("-1的绝对值"+Math.abs(-1));
		System.out.println("1.57的正弦"+Math.sin(1.57));
		System.out.println("4的平方根"+Math.sqrt(4));
		System.out.println("27的立方根"+Math.cbrt(27));
		System.out.println("乘方值"+Math.pow(2, 3));
		System.out.println("3.8的最小整数"+Math.ceil(3.8));
		System.out.println("2.4的最大整数"+Math.floor(2.4));
		System.out.println("对小数进行四舍五入的结果"+Math.round(-3.6));
		System.out.println("2.4的最大整数"+Math.floor(2.4));
		System.out.println("对小数进行取整的结果"+(int)(-3.6));
	}

}
